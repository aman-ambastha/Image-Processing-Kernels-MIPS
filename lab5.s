transform:
############################### Part 2: your code begins here ###########################
addi $t6, $zero, 0 # Consider this as x coordinate for output image (Column Index)
addi $t8, $zero, 0 # X0 coordinate for input image
addi $t9, $zero, 0 # Y0 coordinate for input image
FirstLoop:
   bge $t6, $a3, End2 # Outer while loop iterates until x < a3 - 1 or all columns and every pixel in every column of the output image have been visited
   addi $t7, $zero, 0 # Consider this as y coordinate for output image (Row Index)
 
SecondLoop:
   bge $t7, $a3, Increment # Inner while loop iterates until all rows for one column are visited. EXAMPLE : (row, column), (y , x) -> (0,0) (1,0) and so on
   
   lw $t0, 0($a2) # M00. Extract Value From Matrix 
   lw $t1, 4($a2) # M01. Extract Value From Matrix 
   lw $t2, 8($a2) # M02. Extract Value From Matrix
   
   # Calculate X0 coordinate. X0 is the x coordinate (Column Index) of input image pixel for output at x in the output image. IDEA: Pixel Value of output image at (row, column)or (y , x) = Pixel Value of input image at (row, column) or (Y0, X0)
   mul $t8, $t7, $t0 # M00 * y
   mul $t3, $t6, $t1 # M01 * x
   add $t8, $t8, $t3 # (M00 * y) + (M01 * x)
   add $t8, $t8, $t2 # X0 = (M00 * y) + (M01 * x) + M02
   
   lw $t0, 12($a2) # M10. Extract Value From Matrix 
   lw $t1, 16($a2) # M11. Extract Value From Matrix
   lw $t2, 20($a2) # M12. Extract Value From Matrix
   
   # Calculate Y0 coordinate. Y0 is the y coordinate (Row Index) of input image pixel for output at y in the output image.  IDEA: Pixel Value of output image at (row, column)or (y , x) = Pixel Value of input image at (row, column) or (Y0, X0)
   mul $t9, $t7, $t0 # M10 * y
   mul $t3, $t6, $t1 # M11 *x
   add $t9, $t9, $t3 # (M10 * y) + (M11 * x)
   add $t9, $t9, $t2 # Y0 = (M10 * y) + (M11 * x) + M12
   
   bge $t8, $a3, Over # If X0 coordinate is not in dimension (X0 >= a3)
   bge $t9, $a3, Over # If Y0 coordinate is not in dimension (Y0 >= a3)
   j Under # Otherwise jump to under label to get pixel value at (row (Y0), column (X0)) from memory using base address and offset value

Under:   
   mul $t4, $t9, $a3 # Offset = row index * num of columns + column index (Offset = Y0 * a3) + X0
   add $t4, $t4, $t8
   add $t4, $t4, $a0 # Memory Address = Offset + Base Address (Held In $a0)
   
   lbu $t5, 0($t4) # Load Pixel Value from memory that corresponds to (Y0, X0)
   sb $t5, 0($a1) # Store it to output buffer
   
   addi $a1, $a1, 1 # Increment Output Buffer Address
   addi $t7, $t7, 1 # Increment Row Index
   j SecondLoop # Jump to beginning of inner while loop

Over:
  addi $t4, $zero, 0x00 # If row index of input image (Y0) or column index of input image (X0) >= a3, than make pixel at corresponding row and column index of output image 0
  sb $t4, 0($a1) # Store 0 into memory output buffer address
  addi $a1, $a1, 1 # Increment Output Buffer Address
  addi $t7, $t7, 1 # Increment Row Index
  j SecondLoop # Jump to beginning of inner while loop

Increment:
  addi $t6, $t6, 1 # Increment Column Index
  j FirstLoop # Jump to beginning of outer while loop

End2:

