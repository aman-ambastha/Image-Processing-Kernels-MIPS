threshold: 
############################## Part 1: your code begins here ###
mult $a2, $a2 # Multiply $a2 * $a2 to get total number of pixels
mflo $t1 # Move answer to $t1
addi $t0, $zero, 0 # $t0 register to count number of pixels visited
PixelCount:
   bge $t0, $t1, End # If all pixels have been traversed through and value moved to 0xFF or 0x00
   lbu $t2, 0($a0) # Load pixel value 
   bge $t2, $a3, Larger # If pixel value is greater than the threshold
   blt $t2, $a3, Smaller # If pixel value is less than the threshold
   
Larger:
   addi $t2, $zero, 0xFF # Move pixel to maximum 0xFF
   sb $t2, 0($a1) # Store pixel value into output buffer
   addi $a0, $a0, 1 # Increase input buffer address to load next pixel value
   addi $a1, $a1, 1 # Increase output buffer address to store next pixel value after changed to 0x00 or 0xFF
   addi $t0, $t0, 1 # Add 1 to the number of pixels traversed
   j PixelCount # Jump to beginning of loop
   
Smaller:
  addi $t2, $zero, 0x00 # Move pixel to minimum 0x00
  sb $t2, 0($a1) # Store pixel value into memory output buffer
  addi $a0, $a0, 1 # Increase input buffer address to load next pixel value
  addi $a1, $a1, 1 # Increase output buffer address to store next pixel value after changed to 0x00 or 0xFF
  addi $t0, $t0, 1 # Add 1 to the number of pixels traversed
  j PixelCount	# Jump to beginning of loop
  
End: # Label jumped to if all pixels in input buffer have been traversed and moved to 0x00 or 0xFF
	
